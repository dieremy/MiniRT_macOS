#include "libft.h"

int	ft_perror(char *s, int f)
{
	if (f == 0)
	{
		while (*s)
			write(1, &(*s++), 1);
		exit(0);
	}
	else
	{
		while (*s)
			write(2, &(*s++), 1);
		exit(1);
	}
}
